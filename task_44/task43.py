def следующая_дата():
    D = int(input("Введите день (1-31): "))
    M = int(input("Введите месяц (1-12): "))
    дни_в_месяце = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]

    if 1 <= M <= 12 and 1 <= D <= дни_в_месяце[M - 1]:
        D += 1
        if D > дни_в_месяце[M - 1]:
            D = 1
            M += 1
            if M > 12:
                M = 1
        print(f"Следующая дата: {D}.{M}")
    else:
        print("ошибка")


