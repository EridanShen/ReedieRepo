def описание_числа():
    число = int(input("Введите число (100-999): "))
    if 100 <= число <= 999:
        единицы = ["", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять"]
        десятки = ["", "десять", "двадцать", "тридцать", "сорок", "пятьдесят", "шестьдесят", "семьдесят", "восемьдесят",
                   "девяносто"]
        сотни = ["", "сто", "двести", "триста", "четыреста", "пятьсот", "шестьсот", "семьсот", "восемьсот", "девятьсот"]
        исключения = ["десять", "одиннадцать", "двенадцать", "тринадцать", "четырнадцать", "пятнадцать", "шестнадцать",
                      "семнадцать", "восемнадцать", "девятнадцать"]

        s = число // 100
        d = (число // 10) % 10
        e = число % 10

        if d == 1 and e != 0:
            результат = f"{сотни[s]} {исключения[e]}"
        else:
            результат = f"{сотни[s]} {десятки[d]} {единицы[e]}"

        # Удаление лишних пробелов
        результат = ' '.join(результат.split())

        print(результат.strip())
    else:
        print("ошибка")



